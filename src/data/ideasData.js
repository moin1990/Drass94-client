export const getIdeaById = (id) => ideasData.find((idea) => idea._id === id) || null;

export const getTrendingIdeas = (limit = 6) =>
  [...ideasData].sort((a, b) => b.views - a.views).slice(0, limit);

export const getFilteredIdeas = ({ search = '', category = 'All', from = '', to = '' } = {}) => {
  return ideasData.filter((idea) => {
    const matchesSearch = !search || idea.title.toLowerCase().includes(search.toLowerCase());
    const matchesCategory = !category || category === 'All' || idea.category === category;
    const matchesFrom = !from || new Date(idea.createdAt) >= new Date(from);
    const matchesTo = !to || new Date(idea.createdAt) <= new Date(to);
    return matchesSearch && matchesCategory && matchesFrom && matchesTo;
  });
};
